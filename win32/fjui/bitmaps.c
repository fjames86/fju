static HBITMAP makebitmap( void *bits, int bitwidth, int bpp ) {
    HBITMAP hbm;
    BITMAPINFO bmi;
    void *pbits;
	HDC hdc;

    memset( &bmi, 0, sizeof(bmi) );
    bmi.bmiHeader.biBitCount = bpp;
    bmi.bmiHeader.biHeight = bitwidth;
    bmi.bmiHeader.biPlanes = 1;
    bmi.bmiHeader.biSize = sizeof(bmi.bmiHeader);
    bmi.bmiHeader.biWidth = bitwidth;

	hdc = GetDC( 0 );
	hbm = CreateCompatibleBitmap( hdc, bitwidth, bitwidth );
    //hbm = CreateDIBSection( hdc, &bmi, DIB_RGB_COLORS, &pbits, NULL, 0 );
	//memcpy( pbits, bits, (bpp/8)*bitwidth*bitwidth );
    SetDIBits( NULL, hbm, 0, bitwidth, bits, &bmi, BI_RGB );
    ReleaseDC( 0, hdc );

    return hbm;
}

