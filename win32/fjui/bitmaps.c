static struct {
	char *name;
	void *bits;
	int bitwidth;
	HBITMAP hbm;
	HICON hicon;
} bitmaps[16] = {
	{ "fjui", fjui_bits, 32, NULL, NULL },
	{ "reg_key", reg_key_bits, 16, NULL, NULL },
	{ "reg_value", reg_value_bits, 16, NULL, NULL },
	{ NULL, NULL, NULL, NULL }
};

static HBITMAP makebitmap( void *bits, int bitwidth ) {
    HBITMAP hbm;
    BITMAPINFO bmi;
    void *pbits;

    memset( &bmi, 0, sizeof(bmi) );
    bmi.bmiHeader.biBitCount = 32;
    bmi.bmiHeader.biHeight = bitwidth;
    bmi.bmiHeader.biPlanes = 1;
    bmi.bmiHeader.biSize = sizeof(bmi.bmiHeader);
    bmi.bmiHeader.biWidth = bitwidth;

    hbm = CreateDIBSection( NULL, &bmi, 0, &pbits, NULL, 0 );
    SetDIBits( NULL, hbm, 0, bitwidth, bits, &bmi, BI_RGB );
    
    return hbm;
}
static HICON makeicon( HBITMAP hbm ) {
    HICON hicon;
    ICONINFO ico;

    memset( &ico, 0, sizeof(ico) );
    ico.fIcon = TRUE;
    ico.hbmMask = hbm;
    ico.hbmColor = hbm;
    hicon = CreateIconIndirect( &ico );
    
    return hicon;
}

void fjui_init_bitmaps( void ) {
	int i;
	i = 0;
	while( bitmaps[i].name ) {
		bitmaps[i].hbm = makebitmap( bitmaps[i].bits, bitmaps[i].bitwidth );
		bitmaps[i].hicon = makeicon( bitmaps[i].hbm );
		i++;
	}
}

HBITMAP fjui_get_bitmap( char *name ) {
	int i;

	i = 0;
	while( bitmaps[i].name ) {
		if( strcasecmp( bitmaps[i].name, name ) == 0 ) return bitmaps[i].hbm;
		i++;
	}
	return NULL;
}
HICON fjui_get_icon( char *name ) {
	int i;
	i = 0;
	while( bitmaps[i].name ) {
		if( strcasecmp( bitmaps[i].name, name ) == 0 ) return bitmaps[i].hicon;
		i++;
	}
	return NULL;
}

HIMAGELIST fjui_create_imagelist( char **images, int nimages, int bitwidth ) {
	HIMAGELIST himl;
	int i;

	himl = ImageList_Create( bitwidth, bitwidth, FALSE, nimages, 1 );
	for( i = 0; i < nimages; i++ ) {
		ImageList_Add( himl, fjui_get_bitmap( images[i] ), NULL );
	}

	return himl;
}
